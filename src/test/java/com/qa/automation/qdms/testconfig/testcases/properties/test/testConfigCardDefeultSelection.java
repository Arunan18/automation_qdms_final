public class testConfigCardDefeultSelection extends DriverIntialization {
	@Test
	public void testconfigCardDefeultSelection() throws InterruptedException, IOException {
		//login test
		LoginTest.Login();
		
		//test config click
		defeult();
		
		//LOGOUT TEST
		Logout.LogoutFunction();
		
	}
	public void defeult() {
		TestConfigPage tcp=new TestConfigPage();
		PageFactory.initElements(driver, tcp);
		
		try {
			TestConfigPage.testconfigurationcard.click();
			Thread.sleep(1000);
			functionality.defaultSelection(" Checking Default Test Configuration Button Default Screen (When click Configuration Button) :",TestConfigPage.TestBtn ,TestConfigPage.testtableheading, "Material Test");
		} catch (NoSuchElementException e) {
			testCase = extent.createTest("Checking The TEST CONFIGRATION");
            testCase.log(Status.INFO, "Unable to Locate the element 'TEST CONFIGRATION'");
            testCase.log(Status.FAIL, "TEST FAIL ❎");
        }
        catch (ElementClickInterceptedException e) {
        	testCase = extent.createTest("Checking The TEST CONFIGRATION");
            testCase.log(Status.INFO, "Unable to Click on the element 'TEST CONFIGRATION'");
            testCase.log(Status.FAIL, "TEST FAIL ❎");
        }
        catch (Exception e) {
        	testCase = extent.createTest("Checking The TEST CONFIGRATION");
            testCase.log(Status.FAIL, "TEST FAIL ❎"); 
        }
		
	}

}
